            // so unsigned CI builds (and fresh checkouts) still succeed.
            val hasSigning = keystorePropertiesFile.exists() ||
                System.getenv("BADGEBOT_KEYSTORE_FILE") != null
            if (hasSigning) {
                signingConfig = signingConfigs.getByName("release")
            }
        }
    }

